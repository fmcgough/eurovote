$(function()
{
    now.receiveUpdate = function(data)
    {
        console.log("Received update: " + JSON.stringify(data));

        var newrows;
        for (var i = 0; i < data.length; i++)
        {
            newrows += "<tr><td>"
                    + "<span><img src=\"/images/" + data[i].country + ".png\" class=\"flag-small\"></span>"
                    + data[i].country
                    + "</td><td>"
                    + data[i].score
                    + "</td></tr>";
        }
        $("#results tbody").html(newrows);
    };

});